"""
    Funções:

    definição:
    def nome(parametro):
        bloco de codigo

    execução:
    nome(argumentos)

"""
def soma(x, y):
    return x + y

def mult(x, y):
    return x * y

s = soma(2, 4)
m = mult(3, 4)

print('soma: ', s)
print('mult: ', m)

print('somando dos result: ', soma(s, m))
